#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int k;

	i = 0;
	k = 1;
	while (argv[k][i] != '\0')
	{
		i++;
	}
	while (i >= 0)
	{
		i--;
		write(1, &argv[k][i], 1);
	}
	return (0);
}
